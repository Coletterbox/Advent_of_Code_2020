def CountTrees(input, colDelta: int, rowDelta: int):
    treeCount = 0
    rowWidth = len(input[0])
    currentCoord = {"col": 0, "row": 0}
    while currentCoord["row"] < len(input) - 1:
        currentCoord["col"] = (currentCoord["col"] + colDelta) % rowWidth
        currentCoord["row"] += rowDelta
        if input[currentCoord["row"]][currentCoord["col"]] == '#':
            treeCount += 1
    return treeCount

